package iNeuron_Assignment_1;

public class Question_2 {

	public static void main(String[] args) {
		int n=4; // Taking the number of Rows.
		
		// Printing the pattern
		for (int i=1; i<=n; i++) {
			for (int j=1; j<=n; j++) {
				System.out.print(i);
			}
			System.out.println(""); // Printing new line after each row
		}

	}

}
